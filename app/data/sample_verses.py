def get_sample_verses():
    """Retorna os versículos de exemplo"""
    return SAMPLE_VERSES

def load_sample_verses_to_db():
    """Carrega os versículos de exemplo no banco de dados"""
    from app.database.local_connection import db_manager
    
    try:
        for verse in SAMPLE_VERSES:
            # Verificar se o versículo já existe
            check_query = """
            SELECT COUNT(*) as count FROM bible_verses 
            WHERE book_code = :book_code AND chapter = :chapter AND verse = :verse AND version = :version
            """
            
            result = db_manager.fetch_all(check_query, verse)
            
            if result and result[0]['count'] == 0:
                # Inserir versículo se não existir
                insert_query = """
                INSERT INTO bible_verses (book_code, chapter, verse, text, version)
                VALUES (:book_code, :chapter, :verse, :text, :version)
                """
                
                db_manager.execute_query(insert_query, verse)
        
        return True
        
    except Exception as e:
        print(f"Erro ao carregar versículos de exemplo: {e}")
        return False